#pragma once

// -----------------------------------------------------------------------------
// app_types.h
// Общие типы домена: настройки, текущее состояние автомобиля и состояния
// программы (машина состояний протокола).
// -----------------------------------------------------------------------------

#include "app_config.h"

struct Settings {
    // Емкость топливного бака в литрах
    float fuel_tank_capacity = 60.0f;

    // Количество форсунок в модели
    int injector_count = 4;

    // Производительность одной форсунки (как "cc/min" по текущей модели)
    float injector_performance = 250.0f;

    // Служебный параметр (сейчас не используется в расчётах, но принимается
    // от Android как часть калибровок)
    int speed_sensor_signals = 2000;

    // Начальный уровень топлива (л) при получении settings от Android
    float initial_fuel = 60.0f;
};

struct CarState {
    // Текущая скорость (км/ч)
    float speed = 0.0f;

    // Текущие обороты двигателя (RPM)
    float rpm = RPM_IDLE;

    // "Напряжение" (виртуальный параметр, маппинг из RPM)
    float voltage = VOLTAGE_IDLE;

    // Пробег/одометр на поездку A (км)
    float trip_a = 0.0f;

    // Пробег/одометр на поездку B (км)
    float trip_b = 0.0f;

    // Одометр (общий пробег) (км)
    float odometer = 12450.7f;

    // Суммарно использованное топливо (л) с начала накопления
    float fuel_used_total = 0.0f;

    // Текущий уровень топлива (л), пересчитывается из fuel_used_total
    float fuel_level = 60.0f;

    // Текущий мгновенный расход (л/100 км в текущей формуле)
    float fuel_consumption = 0.0f;

    // Запас хода (км), пересчитывается как fuel_level / fuel_consumption * 100
    float remaining_range = 0.0f;

    // Передача (упрощённая 4-ступенчатая АКПП), 1..4
    int gear = 1;
};

// -----------------------------------------------------------------------------
// ProgramState
// Состояние логики программы (как она должна реагировать на команды и слать телеметрию)
// -----------------------------------------------------------------------------
enum ProgramState {
    // Нет клиента / ждём подключение
    STATE_WAITING_CONNECTION,

    // Подключились, ждём settings (калибровки/начальные значения)
    STATE_WAITING_SETTINGS,

    // Остановлены расчёты/стрим телеметрии, но готовы принимать команды
    STATE_IDLE,

    // Активен стриминг данных (телеметрия отправляется периодически)
    STATE_STREAMING_DATA
};
